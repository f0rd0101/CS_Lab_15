#include <stdio.h>

int main() {
    int arr[10];            // Масив з 10 цілих чисел
    int *p, *pmax;          // Вказівники для перебору масиву
    int i, n;

    printf("n = ");
    scanf("%d", &n);        // Введення кількості елементів

    printf("Введіть елементи масиву:\n");
    for (i = 0; i < n; i++)
        scanf("%d", &arr[i]); // Введення елементів масиву

    pmax = arr;             // Початково максимум — перший елемент масиву

    for (p = arr + 1; p < arr + n; p++) // Перебір з другого елемента
        if (*p > *pmax)
            pmax = p;       // Оновлення вказівника на максимум

    printf("Максимальний елемент = %d\n", *pmax);       // Значення максимуму
    printf("Його номер = %d\n", pmax - arr + 1);        // Порядковий номер (1-вий індекс)
}
